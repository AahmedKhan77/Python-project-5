course = 'Python for Beginners'

#1. General purpose functions
print(len(course))#counts length of variable

#2. Methods
#'.' variable funtions used as methods for paticular tasks
print(course.upper())#Converts all to uppercase
print(course.lower())#Converts all to lowercase
print(course.capitalize())#Converts all to lowercase except first letter
print(course.split())#displays all words seperately
print(course.find('nn'))#finds any item or word in the string by its index no
print(course.replace('Beginners','Absolute Beginners'))#replaces anthing
print(course.replace('P','J'))#replaces anthing

#3. Boolean functions
print('Python' in course)#observes weather 'xyz' is in the variable or not
print('python' in course)#observes weather 'xyz' is in the variable or not